"""
Tipo None

O tipo de dado None em Python representa o tipo sem tipo, ou poderia ser conhecido também como tipo vazio,
porém, falar que é um tipo sem tipo é o mais aprorpriado.

OBS:
- O tipo None é sempre específicado com a primeira letra maiúscula.
Certo: None
Errado: none
- o tipo None é sempre considerado como false.
Quando utilizamos ?
 - Podemos utilizar None quando queremos criar uma variável e inicializá-la com um tipo sem tipo,
 antes de recebermmos um valor final.
"""

numeros = None

print(numeros)
print(type(numeros))

numeros = 1.44, 1.34, 5.67

print(numeros)
print(type(numeros))

